export type MoodKey = keyof typeof EI_COPY.coach;
export type SurpriseKey = keyof typeof EI_COPY.surprise;

export const getRandomCoachResponse = (mood: string): string => {
  const moodKey = (mood.toLowerCase() as MoodKey) in EI_COPY.coach ? mood.toLowerCase() as MoodKey : 'default';
  const responses = EI_COPY.coach[moodKey];
  return responses[Math.floor(Math.random() * responses.length)];
};

export const getRandomSurpriseResponse = (tone: SurpriseKey = 'default'): string => {
  const responses = EI_COPY.surprise[tone] || EI_COPY.surprise.default;
  return responses[Math.floor(Math.random() * responses.length)];
};

export const getMoodExplanation = (mood: string): string => {
  const moodKey = mood.toLowerCase() as keyof typeof EI_COPY.moodExplanations;
  return EI_COPY.moodExplanations[moodKey] || "Let's find foods that match your current state and support your wellbeing!";
};

// Get multiple EI responses for coach
export const getEIResponse = (type: 'coach' | 'surprise', mood: string): string[] => {
  if (type === 'coach') {
    const moodKey = (mood.toLowerCase() as MoodKey) in EI_COPY.coach ? mood.toLowerCase() as MoodKey : 'default';
    const responses = EI_COPY.coach[moodKey];
    // Return 3 random responses
    const shuffled = [...responses].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, 3);
  }
  return [getRandomSurpriseResponse(mood as SurpriseKey)];
};

// Detect mood from text input
export const detectMoodFromText = (text: string): MoodType => {
  const lowerText = text.toLowerCase();
  
  const moodKeywords: Record<MoodType, string[]> = {
    happy: ['happy', 'joy', 'great', 'amazing', 'wonderful', 'excited', 'fantastic', 'good', 'celebrate', 'love'],
    calm: ['calm', 'peaceful', 'relaxed', 'serene', 'zen', 'tranquil', 'chill', 'mellow', 'content'],
    stressed: ['stressed', 'stress', 'overwhelmed', 'pressure', 'deadline', 'busy', 'hectic', 'anxious work'],
    tired: ['tired', 'exhausted', 'sleepy', 'fatigued', 'drained', 'worn out', 'no energy', 'sluggish'],
    energetic: ['energetic', 'pumped', 'motivated', 'active', 'ready', 'alive', 'vibrant', 'workout', 'gym'],
    pms: ['pms', 'cramps', 'period', 'hormonal', 'bloated', 'cycle', 'menstrual'],
    anxious: ['anxious', 'worried', 'nervous', 'panic', 'fear', 'restless', 'uneasy', 'apprehensive'],
    sad: ['sad', 'down', 'blue', 'depressed', 'unhappy', 'melancholy', 'lonely', 'heartbroken', 'gloomy'],
  };

  for (const [mood, keywords] of Object.entries(moodKeywords)) {
    if (keywords.some(keyword => lowerText.includes(keyword))) {
      return mood as MoodType;
    }
  }
  
  return 'calm'; // Default mood
};

type MoodType = 'happy' | 'calm' | 'stressed' | 'tired' | 'energetic' | 'pms' | 'anxious' | 'sad';
